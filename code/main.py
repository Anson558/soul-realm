    def create_groups(self, layouts):
        for layout in layouts:
            for row_index, row in enumerate(layout): 
                    for col_index, col in enumerate(row):
                        col_pos = pygame.math.Vector2((col_index + 5) * tile_size, (row_index + 5) * tile_size)
                        if col != -1:
                            if layout == self.players_csv:
                                self.entities.append(Player(self, col_pos))
                            if layout == self.walls_csv:
                                self.walls.append(Tile(self, col_pos, load_cut_image('level/walls.png')[col]))
                            if layout == self.ground_csv:
                                self.ground.append(Tile(self, col_pos, load_cut_image('level/ground.png')[col]))

